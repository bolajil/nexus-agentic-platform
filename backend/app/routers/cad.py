"""
NEXUS Platform — CAD File Router
==================================
Serves STEP and STL files generated by FreeCAD and Zoo.dev tools.
Includes GD&T tolerance annotations for manufacturing.

Endpoints:
  GET  /api/v1/cad/{session_id}/status       — JSON availability check
  GET  /api/v1/cad/{session_id}/design.step  — STEP file download (FreeCAD)
  GET  /api/v1/cad/{session_id}/design.stl   — STL file download (FreeCAD)
  GET  /api/v1/cad/{session_id}/tolerances   — GD&T tolerance specs (JSON)
  GET  /api/v1/cad/{session_id}/zoo_design.step — STEP file download (Zoo.dev)
  GET  /api/v1/cad/{session_id}/zoo_design.stl  — STL file download (Zoo.dev)
  POST /api/v1/cad/text-to-cad               — Generate CAD from text (Zoo.dev)
  GET  /api/v1/cad/credits                   — Check Zoo.dev API credits
  GET  /api/v1/cad/health                    — Check Zoo.dev API health
"""
from __future__ import annotations

import logging
from pathlib import Path
from typing import List, Optional

from fastapi import APIRouter, HTTPException
from fastapi.responses import FileResponse, JSONResponse
from pydantic import BaseModel, Field

logger = logging.getLogger(__name__)

router = APIRouter(prefix="/cad", tags=["cad"])

CAD_OUTPUT_DIR = Path(__file__).resolve().parent.parent.parent / "cad_output"


def _session_dir(session_id: str) -> Path:
    return CAD_OUTPUT_DIR / session_id


@router.get("/{session_id}/status")
async def cad_status(session_id: str) -> JSONResponse:
    """Check whether CAD files exist for a session (FreeCAD and Zoo.dev)."""
    d = _session_dir(session_id)
    
    # FreeCAD files
    step = d / "design.step"
    stl  = d / "design.stl"
    
    # Zoo.dev files
    zoo_step = d / "zoo_design.step"
    zoo_stl  = d / "zoo_design.stl"
    zoo_gltf = d / "zoo_design.gltf"

    # Read domain from a small metadata file if present
    domain = None
    meta_path = d / "meta.txt"
    if meta_path.is_file():
        domain = meta_path.read_text(encoding="utf-8").strip()

    freecad_available = (step.is_file() and step.stat().st_size > 100) or \
                        (stl.is_file()  and stl.stat().st_size  > 100)
    zoo_available = (zoo_step.is_file() and zoo_step.stat().st_size > 100) or \
                    (zoo_stl.is_file()  and zoo_stl.stat().st_size  > 100)
    
    # Tolerance specs
    tol_file = d / "tolerances.json"
    has_tolerances = tol_file.is_file()
    tolerance_summary = None
    if has_tolerances:
        try:
            import json
            with open(tol_file, "r", encoding="utf-8") as f:
                tol_data = json.load(f)
            tolerance_summary = {
                "iso_class": tol_data.get("iso_tolerance_class"),
                "material": tol_data.get("material", {}).get("name"),
                "surface_finish_ra_um": tol_data.get("surface_finish_ra_um"),
                "num_annotations": len(tol_data.get("critical_dimensions", [])),
            }
        except Exception:
            pass

    return JSONResponse({
        "available":  freecad_available or zoo_available,
        "session_id": session_id,
        "domain":     domain,
        # FreeCAD files
        "freecad": {
            "has_step": step.is_file() and step.stat().st_size > 100,
            "has_stl":  stl.is_file()  and stl.stat().st_size  > 100,
            "step_size": step.stat().st_size if step.is_file() else 0,
            "stl_size":  stl.stat().st_size  if stl.is_file()  else 0,
        },
        # Zoo.dev files
        "zoo_dev": {
            "has_step": zoo_step.is_file() and zoo_step.stat().st_size > 100,
            "has_stl":  zoo_stl.is_file()  and zoo_stl.stat().st_size  > 100,
            "has_gltf": zoo_gltf.is_file() and zoo_gltf.stat().st_size > 100,
            "step_size": zoo_step.stat().st_size if zoo_step.is_file() else 0,
            "stl_size":  zoo_stl.stat().st_size  if zoo_stl.is_file()  else 0,
            "gltf_size": zoo_gltf.stat().st_size if zoo_gltf.is_file() else 0,
        },
        # GD&T tolerances
        "tolerances": {
            "available": has_tolerances,
            "summary": tolerance_summary,
        },
        # Legacy fields for backwards compatibility
        "has_step":   step.is_file() and step.stat().st_size > 100,
        "has_stl":    stl.is_file()  and stl.stat().st_size  > 100,
        "step_size":  step.stat().st_size if step.is_file() else 0,
        "stl_size":   stl.stat().st_size  if stl.is_file()  else 0,
    })


@router.get("/{session_id}/design.step")
async def download_step(session_id: str) -> FileResponse:
    """Download the STEP file for a session."""
    path = _session_dir(session_id) / "design.step"
    if not path.is_file() or path.stat().st_size < 100:
        raise HTTPException(status_code=404, detail="STEP file not available for this session")
    return FileResponse(
        path=str(path),
        media_type="application/step",
        filename=f"nexus_{session_id[:8]}_design.step",
    )



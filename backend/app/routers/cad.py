@router.get("/{session_id}/tolerances")
async def download_tolerances(session_id: str) -> JSONResponse:
    """
    Download GD&T tolerance specifications for a session.
    
    Returns JSON with:
    - ISO tolerance class
    - Material specification
    - Critical dimensions with tolerances
    - Surface finish requirements
    - Manufacturing notes
    """
    path = _session_dir(session_id) / "tolerances.json"
    if not path.is_file():
        raise HTTPException(status_code=404, detail="Tolerance specs not available for this session")
    
    import json
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
    
    return JSONResponse(data)


# ── Zoo.dev Text-to-CAD Endpoints ────────────────────────────────────────────

class TextToCADRequest(BaseModel):
    """Request model for Zoo.dev Text-to-CAD generation."""
    prompt: str = Field(..., min_length=10, max_length=2000, description="Natural language part description")
    session_id: str = Field(..., min_length=1, max_length=100, description="Session ID for file organization")
    output_formats: List[str] = Field(default=["step", "stl"], description="Output formats: step, stl, gltf")


class TextToCADResponse(BaseModel):
    """Response model for Zoo.dev Text-to-CAD generation."""
    success: bool
    step_path: Optional[str] = None
    stl_path: Optional[str] = None
    gltf_path: Optional[str] = None
    generation_time_seconds: float
    estimated_cost_usd: float
    error_message: Optional[str] = None


class CreditsResponse(BaseModel):
    """Response model for Zoo.dev API credits check."""
    available: bool
    free_minutes_remaining: Optional[float] = None
    error: Optional[str] = None


@router.post("/text-to-cad", response_model=TextToCADResponse)
async def generate_cad_from_text(request: TextToCADRequest):
    """
    Generate CAD files from natural language prompt using Zoo.dev API.
    
    Pricing: $0.50/minute, 20 free minutes/month included.
    
    Example prompt: "cylindrical heatsink, 50mm diameter, 30mm tall, with 12 radial fins"
    """
    try:
        from app.tools.zoo_cad_tool import get_zoo_client
    except ImportError as e:
        raise HTTPException(status_code=500, detail=f"Zoo.dev tool not available: {e}")
    
    client = get_zoo_client()
    
    if not client.is_available:
        raise HTTPException(
            status_code=503,
            detail="Zoo.dev API not configured. Set ZOO_API_KEY environment variable."
        )
    
    result = await client.generate(
        prompt=request.prompt,
        session_id=request.session_id,
        output_formats=request.output_formats
    )
    
    return TextToCADResponse(
        success=result.success,
        step_path=result.step_path,
        stl_path=result.stl_path,
        gltf_path=result.gltf_path,
        generation_time_seconds=result.generation_time_seconds,
        estimated_cost_usd=result.estimated_cost_usd,
        error_message=result.error_message
    )


@router.get("/credits", response_model=CreditsResponse)
async def check_zoo_credits():
    """Check remaining Zoo.dev API credits."""
    try:
        from app.tools.zoo_cad_tool import get_zoo_client
    except ImportError:
        return CreditsResponse(available=False, error="Zoo.dev tool not available")
    
    client = get_zoo_client()
    
    if not client.is_available:
        return CreditsResponse(available=False, error="API key not configured")
    
    credits_info = await client.check_credits()
    
    if "error" in credits_info:
        return CreditsResponse(available=False, error=credits_info["error"])
    
    return CreditsResponse(
        available=True,
        free_minutes_remaining=credits_info.get("free_minutes_remaining")
    )


@router.get("/health")
async def cad_health():
    """Check Zoo.dev API connectivity and FreeCAD availability."""
    zoo_available = False
    zoo_url = None
    freecad_available = False
    freecad_path = None
    
    try:
        from app.tools.zoo_cad_tool import get_zoo_client
        client = get_zoo_client()
        zoo_available = client.is_available
        zoo_url = client.base_url if zoo_available else None
    except ImportError:
        pass
    
    try:
        from app.tools.freecad_tool import _find_freecad_exe
        freecad_path = _find_freecad_exe()
        freecad_available = freecad_path is not None
    except ImportError:
        pass
    
    # Fallback: check if any CAD output exists (means FreeCAD worked before)
    if not freecad_available:
        try:
            cad_output = Path(__file__).resolve().parent.parent.parent / "cad_output"
            if cad_output.exists() and any(cad_output.glob("*/design.step")):
                freecad_available = True
                freecad_path = "detected_from_output"
        except Exception:
            pass
    
    return {
        "zoo_api_configured": zoo_available,
        "zoo_api_url": zoo_url,
        "freecad_available": freecad_available,
        "freecad_path": freecad_path,
        "engines": {
            "freecad": "available" if freecad_available else "not_found",
            "zoo_dev": "configured" if zoo_available else "api_key_missing"
        }
    }


